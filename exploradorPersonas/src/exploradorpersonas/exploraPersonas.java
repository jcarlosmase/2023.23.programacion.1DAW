    private void mostrarRegistro(int i){
        //Preparamos los labels
        lX.setText(Integer.toString(i+1));
        lY.setText(Integer.toString(this.gente.size()));
        tDNI.setText(this.gente.get(i).getDNI());
        tNombre.setText(this.gente.get(i).getNombre());
        tApellidos.setText(this.gente.get(i).getApellidos());
        tDireccion.setText(this.gente.get(i).getDireccion());
        //Sexo
        brFemenino.setSelected(this.gente.get(i).isSexo());   //Femenino
        brMasculino.setSelected(!this.gente.get(i).isSexo()); //Masculino
        //Aficiones
        cbCerveza.setSelected(false);
        cbCine.setSelected(false);
        cbPasear.setSelected(false);
        cbProgramar.setSelected(false);
        cbTeatro.setSelected(false);
        cbAmigos.setSelected(false);
        for(String aficion : this.gente.get(i).getAficiones()){
            if(aficion.toLowerCase().equals("tomar cerveza"))
                cbCerveza.setSelected(true);
            else if(aficion.toLowerCase().equals("ir al cine"))
                cbCine.setSelected(true);
            else if(aficion.toLowerCase().equals("pasear"))
                cbPasear.setSelected(true);
            else if(aficion.toLowerCase().equals("ir al teatro"))
                cbTeatro.setSelected(true);
            else if(aficion.toLowerCase().equals("programar"))
                cbProgramar.setSelected(true);
            else if(aficion.toLowerCase().equals("quedar con amigos"))
                cbAmigos.setSelected(true);
        }
        //Mayor de Edad
        cbEdad.setSelectedIndex(this.gente.get(i).isMayorEdad()?1:2);
    }
    
