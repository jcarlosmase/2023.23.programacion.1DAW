    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(this.insertar){ //Insertamos
            String [] aficiones = new String[6];
            int i=0;
            if(cbAmigos.isSelected()){
                aficiones[i] = "Quedar con amigos";
                i++;
            }
            if(cbCerveza.isSelected()){
                aficiones[i] = "Tomar cerveza";
                i++;
            }
            if(cbCine.isSelected()){
                aficiones[i] = "Ir al cine";
                i++;
            }
            if(cbPasear.isSelected()){
                aficiones[i] = "Pasear";
                i++;
            }
            if(cbTeatro.isSelected()){
                aficiones[i] = "Ir al Teatro";
                i++;
            }
            if(cbProgramar.isSelected()){
                aficiones[i] = "Programar";
                i++;
            }
            
            this.gente.add(new persona(tDNI.getText(),
                                       tNombre.getText(),
                                       tApellidos.getText(),
                                       tDireccion.getText(),
                                       brFemenino.isSelected(),
                                       aficiones,
                                       cbEdad.getSelectedIndex()==1?true:false));
            this.insertar = false;
            mostrarRegistro(this.gente.size()-1);
        }else{ //Actualizamos
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

